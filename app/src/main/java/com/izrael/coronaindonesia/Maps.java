package com.izrael.coronaindonesia;

public class Maps {
    private Double Latitude;
    private Double Longtitude;
    private String jumlahorang;
    private String provensi;
    private String status;

    public Double getLongtitude() {
        return Longtitude;
    }

    public void setLongtitude(Double longtitude) {
        Longtitude = longtitude;
    }

    public String getJumlahorang() {
        return jumlahorang;
    }

    public void setJumlahorang(String jumlahorang) {
        this.jumlahorang = jumlahorang;
    }

    public String getProvensi() {
        return provensi;
    }

    public void setProvensi(String provensi) {
        this.provensi = provensi;
    }

    public String getStatus() {
        return status;
    }

    public void setStatus(String status) {
        this.status = status;
    }

    public Double getLatitude() {
        return Latitude;
    }

    public void setLatitude(Double latitude) {
        Latitude = latitude;
    }
}
